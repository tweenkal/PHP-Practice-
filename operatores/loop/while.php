<!-- increment -->

<?php
    $num = 1;
    while($num <= 10)
    {
        echo $num;
        echo "<br>";
        $num++;
    }
    echo "<br>";
    echo "<br>";
?>

<!-- decrement -->

<?php
    $num = 10;
    while($num >= 1)
    {
        echo $num;
        echo "<br>";
        $num--;
    }
?>